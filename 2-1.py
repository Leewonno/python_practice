# 숫자형
# 정수, 실수, 8진수, 16진수

# 정수
a = 123
print(a)
a = -1
print(a)
a = 0
print(a)
# 지수 표현방식
a = 4.24E10
print(a)

# 실수
a = 1.2
print(a)
a = -3.14
print(a)

# 8진수
a = 0o177
print(a)

# 16진수
a = 0xabc
print(a)


